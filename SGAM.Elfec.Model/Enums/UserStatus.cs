namespace SGAM.Elfec.Model.Enums
{
    /// <summary>
    /// Enum de los estados de un usuario
    /// </summary>
    public enum UserStatus
    {
        /// <summary>
        /// Estado de usuario inhabilitado para el uso del sistema
        /// </summary>
        Disabled,
        /// <summary>
        /// Estado de usuario habilitado para el uso del sistema
        /// </summary>
        Enabled,
        /// <summary>
        /// Estado de usuario no registrado para el uso de la aplicación
        /// </summary>
        NonRegistered
    }
}
